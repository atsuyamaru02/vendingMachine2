package com.example.demo.entity;

public class HistoryEntity {
	
	private String name;
	private int price;
	private String hotcold;
	private int quantity;
	private String buydatetime;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getHotcold() {
		return hotcold;
	}
	public void setHotcold(String hotcold) {
		this.hotcold = hotcold;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public String getBuydatetime() {
		return buydatetime;
	}
	public void setBuydatetime(String buydatetime) {
		this.buydatetime = buydatetime;
	}
	
	

}
